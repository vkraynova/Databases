USE SHIPS

SELECT CLASS, COUNTRY
FROM CLASSES
WHERE NUMGUNS < 10


SELECT NAME as shipName
FROM SHIPS
WHERE LAUNCHED < 1918


SELECT SHIP, BATTLE
FROM OUTCOMES
WHERE RESULT = 'sunk'


SELECT NAME
FROM SHIPS
WHERE CLASS = NAME


SELECT NAME
FROM SHIPS
WHERE NAME LIKE 'R%'


SELECT NAME
FROM SHIPS
WHERE NAME LIKE '% %'